char	*ft_strcat(char *dest, char *src)
{
	int	i;
	int	j;
	
	i = 0;
	while (dest[i])
	{
		i++;
	}
	j = 0;
	while (src[j])
	{
		dest[i] = src[j];
		i++;
		j++;
	}
	dest[i] = '\0';
	return (dest);
}

#include <stdio.h>
int	main()
{
	char	src[] = "A 42";
	char	dest[50] = "Bienvenue ";

	ft_strcat(dest, src);
	printf("C'est la destination : %s\n", dest);
}